namespace Formulate.BackOffice
{
    /// <summary>
    /// The tree root types used by Formulate in the back office.
    /// </summary>
    public enum TreeRootTypes
    {
        /// <summary>
        /// An unknown tree root type.
        /// </summary>
        Unknown,

        /// <summary>
        /// The form tree root type.
        /// </summary>
        Forms,

        /// <summary>
        /// The data values tree root type.
        /// </summary>
        DataValues,

        /// <summary>
        /// The layouts tree root type.
        /// </summary>
        Layouts,

        /// <summary>
        /// The validations tree root type.
        /// </summary>
        Validations
    }
}
